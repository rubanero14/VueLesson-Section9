<template>
<form @submit.prevent="submitData">
    <div>
        <label>Name</label>
        <br>
        <input type="text" v-model="enteredName"/>    
    </div>
    <div>
        <label>Phone</label>
        <br>
        <input type="tel" v-model="enteredPhone"/>    
    </div>
    <div>
        <label>Email</label>
        <br>
        <input type="email" v-model="enteredEmail"/>    
    </div>
    <div>
        <button>Add Contact</button>
    </div>
</form>    
</template>

<script>
export default {
    emits: ['add-contact'],
    data(){
        return{
            enteredName: '',
            enteredPhone: '',
            enteredEmail: ''
        }
    },
    methods: {
        submitData(){
            this.$emit(
            'add-contact',
            this.enteredName, 
            this.enteredPhone, 
            this.enteredEmail
            );
        }
    },
};
</script>
